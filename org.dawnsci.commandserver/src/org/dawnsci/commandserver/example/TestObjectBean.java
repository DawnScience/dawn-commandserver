package org.dawnsci.commandserver.example;

import java.io.Serializable;


public class TestObjectBean implements Serializable {
    @Override
	public String toString() {
		return "TestObjectBean [m1=" + m1 + ", m2=" + m2 + "]";
	}
	/**
	 * 
	 */
	private static final long serialVersionUID = -4989930922999152348L;
	private String m1;
    private String m2;
	public TestObjectBean(String m1, String m2) {
		super();
		this.m1 = m1;
		this.m2 = m2;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((m1 == null) ? 0 : m1.hashCode());
		result = prime * result + ((m2 == null) ? 0 : m2.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TestObjectBean other = (TestObjectBean) obj;
		if (m1 == null) {
			if (other.m1 != null)
				return false;
		} else if (!m1.equals(other.m1))
			return false;
		if (m2 == null) {
			if (other.m2 != null)
				return false;
		} else if (!m2.equals(other.m2))
			return false;
		return true;
	}
	public String getM1() {
		return m1;
	}
	public void setM1(String m1) {
		this.m1 = m1;
	}
	public String getM2() {
		return m2;
	}
	public void setM2(String m2) {
		this.m2 = m2;
	}
}
